// Reverse mapping for search (Persian to code)
export const FA_TO_CODE = Object.entries(COUNTRIES_FA).reduce((acc, [code, name]) => {
  acc[name] = code
  return acc
}, {})

// Get Persian name by code
export const getPersianName = (code) => {
  return COUNTRIES_FA[code] || code
}

// Search countries by Persian name
export const searchCountries = (query) => {
  if (!query) return []
  return Object.entries(COUNTRIES_FA)
    .filter(([code, name]) => name.includes(query))
    .map(([code, name]) => ({ code, name }))
}

